// 알파벳으로 이루어진 문자열 myString이 주어짐. 모든 알파벳을 대문자로 변환하여 return

function changeUpperCase(myString: string): string {
  return myString.toUpperCase();
}

console.log(changeUpperCase("sjdkDfkjG"));
console.log(changeUpperCase("sjdkfjdkf"));
